public class WhileLoops {
    // to print the natural no. from 1 to 10 
    public static void main(String[] args) {
    int i = 1 ;
    int sum = 0;
    while( i<=10 ){
        
        System.out.println(i);
        i++;
        sum+=i;
       
    }
    System.out.println(sum);
    }

    
    
}
